/**
 * Clase utilitaria para operacións matemáticas básicas.
 *
 * @author carlosml
 * @version 1.0
 */
public class Calculadora {

    /**
     * Suma dous valores enteiros e devolve o resultado.
     *
     * @param a primeiro valor
     * @param b segundo valor
     * @return suma de a e b
     */
    public static int sumar(int a, int b) {
        return a + b;
    }
}
